BOOLEAN
AnscAsn1RSAPublicKeyIsKeyMatching
    (
        ANSC_HANDLE                 hThisObject,
        PKI_KEY_TYPE                keyType,
        ANSC_HANDLE                 hKeyParams
    )
{
    PANSC_ASN1_RSAPUBLICKEY         pThisObject     = (PANSC_ASN1_RSAPUBLICKEY)hThisObject;
    PANSC_CRYPTO_PUB_KEY_GEN_PARAMS pGenParams      = (PANSC_CRYPTO_PUB_KEY_GEN_PARAMS)hKeyParams;
    PANSC_ASN1_INTEGER              pInteger;

    if( hThisObject == NULL || hKeyParams == NULL || keyType != PKI_RSA_KEY)
    {
        return FALSE;
    }

    /* check the values */
    pInteger = (PANSC_ASN1_INTEGER)pThisObject->GetChildByIndex(pThisObject,0);

    if(!isIntegerMatching
        (
            (PUCHAR)pInteger->GetValueBuffer(pInteger),
            pInteger->uLength,
            pGenParams->PublicKey.RSA.Modulus.Data,
            pGenParams->PublicKey.RSA.Modulus.Length
        ))
    {
        return FALSE;
    }

    pInteger = (PANSC_ASN1_INTEGER)pThisObject->GetChildByIndex(pThisObject,1);

    if( pInteger->uLength != pGenParams->PublicKey.RSA.PublicExponent.Length)
    {
        return FALSE;
    }

    if(!AnscEqualMemory
        ( 
            pInteger->GetValueBuffer(pInteger), 
            pGenParams->PublicKey.RSA.PublicExponent.Data.ucData,
            pInteger->uLength
        ))
    {
        return FALSE;
    }

    return TRUE;
}

