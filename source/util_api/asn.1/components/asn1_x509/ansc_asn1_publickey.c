ANSC_STATUS
AnscAsn1KeyPairBeforeDecodingChild
    (
        ANSC_HANDLE                 hThisObject,
        int                         index,
        PVOID*                      ppEncoding
    )
{
    UNREFERENCED_PARAMETER(ppEncoding);
    PANSC_ASN1_KEYPAIR              pThisObject  = (PANSC_ASN1_KEYPAIR)hThisObject;
    PANSC_ASN1_INTEGER              pKeyType;
    PANSC_ASN1_CHOICE               pPubKey;

    if( index == 1)
    {
        /* check the key type */
        pKeyType = (PANSC_ASN1_INTEGER)
            pThisObject->GetChildByIndex(pThisObject, 0);

        pPubKey  = (PANSC_ASN1_CHOICE)
            pThisObject->GetChildByIndex(pThisObject, 1);

        if( pKeyType->GetIntegerValue(pKeyType) == PKI_RSA_KEY)
        {
            pPubKey->SetAndCreateSelection
                (
                    pPubKey,
                    PUBLICKEY_MASK_RSAPUBLICKEY
                );
        }
        else
        {
            pPubKey->SetAndCreateSelection
                (
                    pPubKey,
                    PUBLICKEY_MASK_DSAPUBLICKEY
                );
        }
    }

    return ANSC_STATUS_SUCCESS;
}

ANSC_STATUS
AnscAsn1KeyPairInitKey
    (
        ANSC_HANDLE                 hThisObject,
        ULONG                       keyType,
        ANSC_HANDLE                 hKeyGenHandle
    )
{
    PANSC_ASN1_KEYPAIR              pThisObject   = (PANSC_ASN1_KEYPAIR)hThisObject;
    PANSC_ASN1_INTEGER              pKeyType      = NULL;
    PANSC_ASN1_PUBLICKEY            pPubKey       = NULL;
    PANSC_ASN1_PRIVATEKEYINFO       pPriKeyInfo   = NULL;
    PANSC_ASN1_RSAPUBLICKEY         pRsaKey       = NULL;
    PANSC_ASN1_DSAPUBLICKEY         pDsaKey       = NULL;

    /* set the key type */
    pKeyType = (PANSC_ASN1_INTEGER)pThisObject->GetChildByIndex(pThisObject, 0);
    pKeyType->SetIntegerValue(pKeyType, keyType);

    /*init the public key */
    pPubKey = (PANSC_ASN1_PUBLICKEY)pThisObject->GetChildByIndex(pThisObject, 1);
    if( keyType == PKI_RSA_KEY)
    {
        pPubKey->SetAndCreateSelection
            (
                pPubKey,
                PUBLICKEY_MASK_RSAPUBLICKEY
            );

        pRsaKey = (PANSC_ASN1_RSAPUBLICKEY)pPubKey->hSelection;

        pRsaKey->InitKey(pRsaKey, hKeyGenHandle);

    }
    else if( keyType == PKI_DSA_KEY)
    {
        pPubKey->SetAndCreateSelection
            (
                pPubKey,
                PUBLICKEY_MASK_DSAPUBLICKEY
            );

        pDsaKey = (PANSC_ASN1_DSAPUBLICKEY)pPubKey->hSelection;

        pDsaKey->InitKey(pDsaKey, hKeyGenHandle);
    }
    else
    {
        AnscTrace("Unknown key type.\n");

        return ANSC_STATUS_FAILURE;
    }

    /* set the private key info */
    pPriKeyInfo = (PANSC_ASN1_PRIVATEKEYINFO)pThisObject->GetChildByIndex(pThisObject, 2);

    pPriKeyInfo->InitKey(pPriKeyInfo, keyType, hKeyGenHandle);

    return ANSC_STATUS_SUCCESS;
}

ANSC_STATUS
AnscAsn1KeyPairExportKey
    (
        ANSC_HANDLE                 hThisObject,
        PULONG                      puKeyType,
        ANSC_HANDLE                 hKeyGenHandle
    )
{
    PANSC_ASN1_KEYPAIR              pThisObject   = (PANSC_ASN1_KEYPAIR)hThisObject;
    PANSC_ASN1_INTEGER              pKeyType      = NULL;
    PANSC_ASN1_PUBLICKEY            pPubKey       = NULL;
    PANSC_ASN1_PRIVATEKEYINFO       pPriKeyInfo   = NULL;

    /* get the key type */
    pKeyType = (PANSC_ASN1_INTEGER)pThisObject->GetChildByIndex(pThisObject, 0);
    *puKeyType = pKeyType->GetIntegerValue(pKeyType);

    /*init the public key */
    pPubKey = (PANSC_ASN1_PUBLICKEY)pThisObject->GetChildByIndex(pThisObject, 1);
    pPubKey->ExportPublicKey(pPubKey, hKeyGenHandle);

    /* set the private key info */
    pPriKeyInfo = (PANSC_ASN1_PRIVATEKEYINFO)pThisObject->GetChildByIndex(pThisObject, 2);
    pPriKeyInfo->ExportKey(pPriKeyInfo, hKeyGenHandle);

    return ANSC_STATUS_SUCCESS;
}

