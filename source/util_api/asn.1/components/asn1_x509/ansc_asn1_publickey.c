static
BOOLEAN
isIntegerMatching
    (
        PUCHAR                      pValue1,
        ULONG                       uLength1,
        PUCHAR                      pValue2,
        ULONG                       uLength2
    )
{
    ULONG                           uSign1   = 0;
    ULONG                           uSign2   = 0;

    if( pValue1[0] == 0x00) 
    {
        uSign1 = 1;
    }

    if( pValue2[0] == 0x00)
    {
        uSign2 = 1;
    }

    if( uLength1 - uSign1 != uLength2 - uSign2)
    {
        return FALSE;
    }

    return 
        AnscEqualMemory
            (
                (PVOID)(pValue1 + uSign1),
                (PVOID)(pValue2 + uSign2),
                uLength1 - uSign1
            );
}

