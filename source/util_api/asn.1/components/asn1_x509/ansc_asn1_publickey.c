    pThisObject->CreateSelection    = AnscAsn1PublicKeyCreateSelection;
    pThisObject->GetSelectionName   = AnscAsn1PublicKeyGetSelectionName;
    pThisObject->GetChoiceFromOID   = AnscAsn1PublicKeyGetChoiceByOID;
