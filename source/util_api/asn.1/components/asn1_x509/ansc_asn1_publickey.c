/*
 *  Manually added functions;
 */
ANSC_STATUS
AnscAsn1RSAPublicKeyVerify
    (
        ANSC_HANDLE                 hThisHandle,
        ANSC_HANDLE                 hParams,
        PUCHAR                      pDataSigned,
        ULONG                       lengthOfData,
        SIGNATURE_TYPE              SignatureType,
        PUCHAR                      pSignature,
        ULONG                       uLength
    )
{
    UNREFERENCED_PARAMETER(hParams);
    /* for RSA, hParams is NULL, don't need to care about it */
    PANSC_ASN1_RSAPUBLICKEY         pRSA        = (PANSC_ASN1_RSAPUBLICKEY)hThisHandle;
    ANSC_STATUS                     status;
    PANSC_CRYPTO_RSA_PUBLIC_KEY     pPublic;
    PANSC_CRYPTO_PUB_SSLEAY_OBJECT  pCrypto;
    PANSC_ASN1_INTEGER              pInteger;
    PANSC_CRYPTO_PUB_KEY_PARAMS     pParams;
    int                             nHashMethod;

    if( pRSA == NULL)
    {
        return ANSC_ASN1_BAD_PARAMETER;
    }

    if( uLength == 0 || pSignature == NULL || pDataSigned == NULL)
    {
        return ANSC_ASN1_INVALID_SIGNATURE;
    }

    if( SignatureType != RSA_SHA1_SIGNATURE && SignatureType != RSA_MD5_SIGNATURE
        && SignatureType != RSA_MD2_SIGNATURE && SignatureType != RSA_MD4_SIGNATURE)
    {
        return ANSC_ASN1_NOT_SUPPORTED;
    }

    if( SignatureType == RSA_SHA1_SIGNATURE)
    {
        nHashMethod = RSA_VERIFY_WITH_SHA1;
    }
    else if( SignatureType == RSA_MD2_SIGNATURE)
    {
        nHashMethod = RSA_VERIFY_WITH_MD2;
    }
    else if( SignatureType == RSA_MD4_SIGNATURE)
    {
        nHashMethod = RSA_VERIFY_WITH_MD4;
    }
    else
    {
        nHashMethod = RSA_VERIFY_WITH_MD5;
    }


    pCrypto     = (PANSC_CRYPTO_PUB_SSLEAY_OBJECT)AnscCreateCryptoPubSSLeay(NULL);

    if (pCrypto == NULL)
    {
        return ANSC_ASN1_NO_RESOURCES;
    }

    pParams = (PANSC_CRYPTO_PUB_KEY_PARAMS)AnscAllocateMemory(sizeof(ANSC_CRYPTO_PUB_KEY_PARAMS));

    if( pParams == NULL)
    {
        pCrypto->Remove(pCrypto); 

        return ANSC_ASN1_NO_RESOURCES;
    }

    pPublic    = (PANSC_CRYPTO_RSA_PUBLIC_KEY)&pParams->Key.RSA.RsaPublicKey;

    /* RSA public key */
    pInteger    = (PANSC_ASN1_INTEGER)pRSA->GetChildByIndex(pRSA,0);
    pPublic->Modulus.Length = pInteger->uLength;
    AnscCopyMemory(pPublic->Modulus.Data, pInteger->pValueBuffer, pPublic->Modulus.Length);

    pInteger    = (PANSC_ASN1_INTEGER)pRSA->GetChildByIndex(pRSA,1);
    pPublic->PublicExponent.Length = pInteger->uLength;
    AnscCopyMemory(pPublic->PublicExponent.Data.ucData, pInteger->pValueBuffer,pPublic->PublicExponent.Length);

    /* verify */
    status = 
        pCrypto->RSAHashAndVerify
                (
                    nHashMethod,
                    (PVOID)pDataSigned,
                    lengthOfData,
                    pSignature,
                    uLength,
                    pParams
                );
 
    pCrypto->Remove(pCrypto); 
    AnscFreeMemory(pParams);

    return status;
}

ANSC_STATUS
AnscAsn1RSAPublicKeyEncrypt
    (
        ANSC_HANDLE                 hThisObject,
        PUCHAR                      pDataWillBeEncrypted,
        ULONG                       lengthOfData,
        PUCHAR                      pDataEncrypted,
        PULONG                      pLength
    )
{
    PANSC_ASN1_RSAPUBLICKEY         pRSA        = (PANSC_ASN1_RSAPUBLICKEY)hThisObject;
    ANSC_STATUS                     status;
    PANSC_CRYPTO_RSA_PUBLIC_KEY     pPublic;
    PANSC_CRYPTO_PUB_SSLEAY_OBJECT  pCrypto;
    PANSC_ASN1_INTEGER              pInteger;
    PANSC_CRYPTO_PUB_KEY_PARAMS     pParams;
    ANSC_CRYPTO_PUB_ALGORITHM       Alg;
    ANSC_CRYPTO_PUB_OPERATION       Op;


    if( pRSA == NULL || pDataWillBeEncrypted == NULL || pDataEncrypted == NULL || pLength == NULL)
    {
        return ANSC_ASN1_BAD_PARAMETER;
    }

    pCrypto     = (PANSC_CRYPTO_PUB_SSLEAY_OBJECT)AnscCreateCryptoPubSSLeay(NULL);

    if (pCrypto == NULL)
    {
        return ANSC_ASN1_NO_RESOURCES;
    }

    pParams = (PANSC_CRYPTO_PUB_KEY_PARAMS)AnscAllocateMemory(sizeof(ANSC_CRYPTO_PUB_KEY_PARAMS));

    if( pParams == NULL)
    {
        pCrypto->Remove(pCrypto); 

        return ANSC_ASN1_NO_RESOURCES;
    }

    pPublic    = (PANSC_CRYPTO_RSA_PUBLIC_KEY)&pParams->Key.RSA.RsaPublicKey;

    /* RSA public key */
    pInteger    = (PANSC_ASN1_INTEGER)pRSA->GetChildByIndex(pRSA,0);
    pPublic->Modulus.Length = pInteger->uLength;
    AnscCopyMemory(pPublic->Modulus.Data, pInteger->pValueBuffer, pPublic->Modulus.Length);

    pInteger    = (PANSC_ASN1_INTEGER)pRSA->GetChildByIndex(pRSA,1);
    pPublic->PublicExponent.Length = pInteger->uLength;
    AnscCopyMemory(pPublic->PublicExponent.Data.ucData, pInteger->pValueBuffer,pPublic->PublicExponent.Length);

    /* encrypt it */
    Op  = ANSC_CRYPTO_PUB_OPERATION_ENCRYPT;
    Alg = ANSC_CRYPTO_PUB_ALGORITHM_RSA_ENCRYPTION;
    pParams->Key.RSA.RsaPublicKey    = *pPublic;

    pParams->InputSize  = lengthOfData;
    pParams->pInput     = (PVOID)pDataWillBeEncrypted;

    pParams->OutputSize = *pLength;
    pParams->pOutput    = (PVOID)pDataEncrypted;

    status = 
        pCrypto->Request
            (
                Alg, 
                Op, 
                (ANSC_HANDLE)pParams, 
                NULL, 
                NULL
            );

    if(pLength)
    {
        *pLength = pParams->OutputSize;
    }

    pCrypto->Remove(pCrypto); 
    AnscFreeMemory(pParams);

    return status;
}

