BOOLEAN
AnscAsn1AlgorithmSetAlgOIDStringValue
    (
        ANSC_HANDLE                 hThisObject,
        PCHAR                       pStringValue
    )
{
    PANSC_ASN1_ALGORITHMIDENTIFIER  pThisObject  = (PANSC_ASN1_ALGORITHMIDENTIFIER)hThisObject;
    PANSC_ASN1_OIDEN                pOIDObject;
    PANSC_ASN1_PARAMETERS           pParams;
 
    if( pThisObject == NULL || pStringValue == NULL)
    {
        return FALSE;
    }

    pOIDObject = (PANSC_ASN1_OIDEN)pThisObject->GetChildByIndex(pThisObject,0);

    if( pOIDObject == NULL)
    {
        return FALSE;
    }

    if( ANSC_STATUS_SUCCESS != pOIDObject->SetStringOIDValue(pOIDObject, pStringValue))
    {
        return FALSE;
    }

    /*
     *  Handle the parameters;
     */
    pParams = (PANSC_ASN1_PARAMETERS)pThisObject->GetChildByIndex(pThisObject,1);

    if( pParams == NULL)
    {
        return TRUE;
    }

    if( ANSC_STATUS_SUCCESS != pParams->SetSelectionByOID(pParams, pStringValue))
    {
        AnscTrace("Failed to create selection for OID value - '%s'\n", pStringValue);

        pParams->bOptional = TRUE;

        return TRUE;
    }

    pParams->bOptional = FALSE;

    return TRUE;
}

ANSC_STATUS
AnscAsn1AlgorithmAfterDecodingChild
    (
        ANSC_HANDLE                 hThisObject,
        int                         index,
        PVOID*                      ppEncoding
    )
{
    PANSC_ASN1_ALGORITHMIDENTIFIER  pThisObject  = (PANSC_ASN1_ALGORITHMIDENTIFIER)hThisObject;
    PANSC_ASN1_PARAMETERS           pParams;

    /*
     * pointer to the start of unexamined raw encoding data
     */
    PUCHAR                          pEncodingUnit= (PUCHAR)*ppEncoding;

    if( index != 0)
    {
        return ANSC_STATUS_SUCCESS;
    }

    pParams = (PANSC_ASN1_PARAMETERS)pThisObject->GetChildByIndex(pThisObject,1);

    if( pParams == NULL)
    {
        return ANSC_STATUS_SUCCESS;
    }

    /*
     *  The only reason to implement the function here is for DSA signature algorithm.
     *
     *  In some place it goes with DSA parameters, some ways go with NULL object,
     *  we have to do something here to deal with it.
     */
    if( pEncodingUnit[0] != 0x00 || pEncodingUnit[1] != 0x05 || pParams->GetSelection(pParams) != NULL)
    {
        return ANSC_STATUS_SUCCESS;
    }

    pParams->SetSelection
        (
            pParams, 
            PARAMETERS_MASK_NULLPARMS, 
            pParams->CreateSelection(pParams,PARAMETERS_MASK_NULLPARMS)
        );

    return ANSC_STATUS_SUCCESS;
}

