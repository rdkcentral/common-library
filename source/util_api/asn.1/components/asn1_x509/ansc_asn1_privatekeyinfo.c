BOOLEAN
AnscAsn1PrivateKeyInfoInitKey
    (
        ANSC_HANDLE                 hThisObject,
        PKI_KEY_TYPE                keyType,
        ANSC_HANDLE                 hKeyGenHandle
    )
{
    PANSC_ASN1_PRIVATEKEYINFO       pThisObject  = (PANSC_ASN1_PRIVATEKEYINFO)hThisObject;
    PANSC_CRYPTO_PUB_KEY_GEN_PARAMS pGenParams   = (PANSC_CRYPTO_PUB_KEY_GEN_PARAMS)hKeyGenHandle;
    PANSC_ASN1_PRIVATEKEY           pPrivateKey;
    PANSC_ASN1_ALGORITHMIDENTIFIER  pAlgor;
    PANSC_ASN1_DSS_PARMS            pDSSParams;
    PANSC_ASN1_CHOICE               pParamChoice;
    PANSC_ASN1_INTEGER              pInteger;

    if( hThisObject == NULL || hKeyGenHandle == NULL)
    {
        return FALSE;
    }

    /* set the oid */
    pAlgor = (PANSC_ASN1_ALGORITHMIDENTIFIER)
        pThisObject->GetChildByIndex(pThisObject,1);

    if( pAlgor == NULL)
    {
        return FALSE;
    }

    if( keyType == PKI_RSA_KEY)
    {
        pAlgor->SetAlgorOIDStringValue(pAlgor,"1.2.840.113549.1.1.1");
    }
    else if( keyType == PKI_DSA_KEY)
    {
        pAlgor->SetAlgorOIDStringValue(pAlgor,"1.2.840.10040.4.1");
    }
    else
    {
        return FALSE;
    }

    /* if it's DSA, init the parameters */
    if( PKI_DSA_KEY == keyType)
    {
        pParamChoice = (PANSC_ASN1_CHOICE)pAlgor->GetChildByIndex(pAlgor,1);
        pDSSParams = (PANSC_ASN1_DSS_PARMS)pParamChoice->hSelection;        
        
        if( pDSSParams != NULL)
        {
            pInteger = (PANSC_ASN1_INTEGER)pDSSParams->GetChildByIndex(pDSSParams,0);
            pInteger->SetStringValue
                (
                    pInteger,
                    pGenParams->PrivateKey.DSA.ParamP.Data.ucData,
                    pGenParams->PrivateKey.DSA.ParamP.Length
                );

            pInteger = (PANSC_ASN1_INTEGER)pDSSParams->GetChildByIndex(pDSSParams,1);
            pInteger->SetStringValue
                (
                    pInteger,
                    pGenParams->PrivateKey.DSA.ParamQ.Data.ucData,
                    pGenParams->PrivateKey.DSA.ParamQ.Length
                );

            pInteger = (PANSC_ASN1_INTEGER)pDSSParams->GetChildByIndex(pDSSParams,2);
            pInteger->SetStringValue
                (
                    pInteger,
                    pGenParams->PrivateKey.DSA.ParamG.Data.ucData,
                    pGenParams->PrivateKey.DSA.ParamG.Length
                );
        }
        else
        {
            return FALSE;
        }
    }


    /* remove the old one */
    pPrivateKey = (PANSC_ASN1_PRIVATEKEY)pThisObject->GetExtraChild(pThisObject);

    if( pPrivateKey != NULL)
    {
        pPrivateKey->AsnFree(pPrivateKey);
    }

    /* create a new one */
    pPrivateKey = (PANSC_ASN1_PRIVATEKEY)pThisObject->CreateExtraChild(pThisObject);

    if( pPrivateKey == NULL)
    {
        return FALSE;
    }

    pThisObject->pExtraChild = pPrivateKey;

    return pPrivateKey->InitKey(pPrivateKey, keyType, hKeyGenHandle);
}

ANSC_STATUS
AnscAsn1PrivateKeyInfoExportKey
    (
        ANSC_HANDLE                 hThisObject,
        ANSC_HANDLE                 hKeyGenHandle
    )
{
    PANSC_ASN1_PRIVATEKEYINFO       pThisObject  = (PANSC_ASN1_PRIVATEKEYINFO)hThisObject;
    PANSC_CRYPTO_PUB_KEY_GEN_PARAMS pGenParams   = (PANSC_CRYPTO_PUB_KEY_GEN_PARAMS)hKeyGenHandle;
    PANSC_ASN1_PRIVATEKEY           pPrivateKey;
    PKI_KEY_TYPE                    keyType;
    PANSC_ASN1_ALGORITHMIDENTIFIER  pAlgor;
    PANSC_ASN1_DSS_PARMS            pDSSParams;
    PANSC_ASN1_CHOICE               pParamChoice;
    PANSC_ASN1_INTEGER              pInteger;

    if( hThisObject == NULL || hKeyGenHandle == NULL)
    {
        return FALSE;
    }

    keyType = pThisObject->GetKeyType(pThisObject);

    pPrivateKey = (PANSC_ASN1_PRIVATEKEY)pThisObject->GetExtraChild(pThisObject);

    if( pPrivateKey == NULL)
    {
        return FALSE;
    }

    if( keyType == PKI_DSA_KEY)
    {
        /* for DSA key, we need to get the parameters */
        pAlgor = (PANSC_ASN1_ALGORITHMIDENTIFIER)
            pThisObject->GetChildByIndex(pThisObject,1);

        pParamChoice = (PANSC_ASN1_CHOICE)pAlgor->GetChildByIndex(pAlgor,1);
        pDSSParams = (PANSC_ASN1_DSS_PARMS)pParamChoice->hSelection;        

        pInteger = (PANSC_ASN1_INTEGER)pDSSParams->GetChildByIndex(pDSSParams,0);
        pGenParams->PrivateKey.DSA.ParamP.Length = pInteger->uLength;
        AnscCopyMemory
            (
                pGenParams->PrivateKey.DSA.ParamP.Data.ucData, 
                pInteger->pValueBuffer, 
                pInteger->uLength
            );

        pInteger = (PANSC_ASN1_INTEGER)pDSSParams->GetChildByIndex(pDSSParams,1);
        pGenParams->PrivateKey.DSA.ParamQ.Length = pInteger->uLength;
        AnscCopyMemory
            (
                pGenParams->PrivateKey.DSA.ParamQ.Data.ucData, 
                pInteger->pValueBuffer, 
                pInteger->uLength
            );

        pInteger = (PANSC_ASN1_INTEGER)pDSSParams->GetChildByIndex(pDSSParams,2);
        pGenParams->PrivateKey.DSA.ParamG.Length = pInteger->uLength;
        AnscCopyMemory
            (
                pGenParams->PrivateKey.DSA.ParamG.Data.ucData, 
                pInteger->pValueBuffer, 
                pInteger->uLength
            );
    }

    return pPrivateKey->ExportKey(pPrivateKey, hKeyGenHandle);
}

ANSC_STATUS
AnscAsn1PrivateKeyInfoAfterDeocdingChild
    (
        ANSC_HANDLE                 hThisObject,
        int                         index,
        PVOID*                      ppEncoding
    )
{
    UNREFERENCED_PARAMETER(ppEncoding);
    PANSC_ASN1_PRIVATEKEYINFO       pThisObject  = (PANSC_ASN1_PRIVATEKEYINFO)hThisObject;
    PANSC_ASN1_OIDEN                pOIDObj;
    PANSC_ASN1_SEQUENCE             pAlgor;
    CHAR                            pOID[256]    = { 0 };
    PANSC_ASN1_CHOICE               extraChild;
    PANSC_ASN1_OBJECT               pVersion;

    if( index != 0 && index != 1)
    {
        return ANSC_STATUS_SUCCESS;
    }
    
    if( index == 0)
    {
        /* actually the version cannot be optional */
        pVersion = (PANSC_ASN1_OBJECT)pThisObject->GetChildByIndex(pThisObject,0);

        if( pVersion != NULL && pVersion->bOptional)
        {
            pVersion->bOptional = FALSE;
        }
    }

    pAlgor = (PANSC_ASN1_SEQUENCE)pThisObject->GetChildByIndex(pThisObject,1);
    pOIDObj= (PANSC_ASN1_OIDEN)pAlgor->GetChildByIndex(pAlgor, 0);

    pOIDObj->GetStringOIDValue(pOIDObj, pOID);

    extraChild  = (PANSC_ASN1_CHOICE)pThisObject->pExtraChild;

    if(extraChild == NULL)
    {
        pThisObject->pExtraChild = 
            pThisObject->CreateExtraChild(pThisObject);

        extraChild  = (PANSC_ASN1_CHOICE)pThisObject->pExtraChild;
    }

    if( strcasecmp( pOID, "1.2.840.113549.1.1.1") == 0 )
    {
        extraChild->SetAndCreateSelection
            (
                extraChild,
                PRIVATEKEY_MASK_RSAPRIVATEKEY
            );
    }
    else if( strcasecmp( pOID, "1.2.840.10040.4.1") == 0 )
    {
        extraChild->SetAndCreateSelection
            (
                extraChild,
                PRIVATEKEY_MASK_DSAPRIVATEKEY
            );
    }

    return ANSC_STATUS_SUCCESS;
}

ANSC_STATUS
AnscAsn1PrivateKeyInfoSignData
    (
        ANSC_HANDLE                 pCryptHandle,
        PUCHAR                      pDataWillBeSigned,
        ULONG                       lengthOfData,
        SIGNATURE_TYPE              SignatureType,
        PUCHAR                      pDataSigned,
        PULONG                      pLength
    )
{
    PANSC_ASN1_PRIVATEKEYINFO       pThisObject  = (PANSC_ASN1_PRIVATEKEYINFO)pCryptHandle;
    ANSC_CRYPTO_PUB_KEY_GEN_PARAMS  keyParams;
    PANSC_CRYPTO_PUB_KEY_GEN_PARAMS pGenParams   = &keyParams;
    ANSC_CRYPTO_PUB_KEY_PARAMS      params;
    ANSC_STATUS                     retStatus;
    PANSC_CRYPTO_PUB_SSLEAY_OBJECT  pCrypto;
    PKI_KEY_TYPE                    keyType;
    int                             nHashMethod;

    if( pThisObject == NULL)
    {
        return ANSC_STATUS_FAILURE;
    }

    if( pDataWillBeSigned == NULL || 
        lengthOfData <= 0         || 
        pLength == NULL           ||
        pDataSigned == NULL )
    {
        return ANSC_ASN1_BAD_PARAMETER;
    }

    retStatus = 
        pThisObject->ExportKey(pThisObject, pGenParams);

    if( ANSC_STATUS_SUCCESS != retStatus)
    {
        return retStatus;
    }

    pCrypto     = (PANSC_CRYPTO_PUB_SSLEAY_OBJECT)AnscCreateCryptoPubSSLeay(NULL);

    if (pCrypto == NULL)
    {
        return ANSC_ASN1_NO_RESOURCES;
    }

    keyType = pThisObject->GetKeyType(pThisObject);

    /* Sign it */
    if( keyType == PKI_RSA_KEY)
    {
        if( SignatureType == RSA_MD5_SIGNATURE)
        {
            nHashMethod = RSA_SIGN_WITH_MD5;
        }
        else if( SignatureType == RSA_MD2_SIGNATURE)
        {
            nHashMethod = RSA_SIGN_WITH_MD2;
        }
        else if( SignatureType == RSA_MD4_SIGNATURE)
        {
            nHashMethod = RSA_SIGN_WITH_MD4;
        }
        else
        {
            nHashMethod = RSA_SIGN_WITH_SHA1;            
        }
        
        params.Key.RSA.RsaPrivateKey = pGenParams->PrivateKey.RSA;

        retStatus = 
            pCrypto->RSAHashAndSign
                    (
                        nHashMethod,
                        (PVOID)pDataWillBeSigned,
                        lengthOfData,
                        pDataSigned,
                        pLength,
                        &params
                    );
    }
    else if( keyType == PKI_DSA_KEY)
    {
        nHashMethod = DSA_SHA1_SIGNATURE;
        params.Key.DSA.DsaPrivateKey = pGenParams->PrivateKey.DSA;

        retStatus = 
            pCrypto->DSAHashAndSign
                    (
                        nHashMethod,
                        (PVOID)pDataWillBeSigned,
                        lengthOfData,
                        pDataSigned,
                        pLength,
                        &params
                    );
    }
    else
    {
        retStatus = ANSC_ASN1_NOT_SUPPORTED;
    }

    pCrypto->Remove(pCrypto); 

    return retStatus;
}

