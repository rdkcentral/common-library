ANSC_STATUS
AnscAsn1RSAPrivateKeyDecryptData
    (
        ANSC_HANDLE                 pCryptHandle ,
        PUCHAR                      pDataWillBeDecrypted,
        ULONG                       lengthOfData,
        PUCHAR                      pDataDecrypted,
        PULONG                      pLength
    )
{
    PANSC_ASN1_RSAPRIVATEKEY        pRSA          = (PANSC_ASN1_RSAPRIVATEKEY)pCryptHandle;
    ANSC_STATUS                     status;
    PANSC_CRYPTO_RSA_PRIVATE_KEY    pPrivate;
    PANSC_CRYPTO_PUB_SSLEAY_OBJECT  pCrypto;
    PANSC_ASN1_INTEGER              pInteger;
    PANSC_CRYPTO_PUB_KEY_PARAMS     pParams;
    ANSC_CRYPTO_PUB_ALGORITHM       Alg;
    ANSC_CRYPTO_PUB_OPERATION       Op;

    if( pRSA == NULL        || pDataWillBeDecrypted == NULL || 
        lengthOfData <= 0   || pLength == NULL              ||
        pDataDecrypted == NULL )
    {
        return ANSC_ASN1_BAD_PARAMETER;
    }

    pCrypto     = (PANSC_CRYPTO_PUB_SSLEAY_OBJECT)AnscCreateCryptoPubSSLeay(NULL);

    if (pCrypto == NULL)
    {
        return ANSC_ASN1_NO_RESOURCES;
    }

    pParams = (PANSC_CRYPTO_PUB_KEY_PARAMS)AnscAllocateMemory(sizeof(ANSC_CRYPTO_PUB_KEY_PARAMS));

    if( pParams == NULL)
    {
        pCrypto->Remove(pCrypto); 

        return ANSC_ASN1_NO_RESOURCES;
    }

    pPrivate    = (PANSC_CRYPTO_RSA_PRIVATE_KEY)&pParams->Key.RSA.RsaPrivateKey;

    /* RSA private key */
    pInteger    = (PANSC_ASN1_INTEGER)pRSA->GetChildByIndex(pRSA,1);
    pPrivate->Modulus.Length = pInteger->uLength;
    AnscCopyMemory(pPrivate->Modulus.Data, pInteger->pValueBuffer, pInteger->uLength);

    pInteger    = (PANSC_ASN1_INTEGER)pRSA->GetChildByIndex(pRSA,2);
    pPrivate->PublicExponent.Length = pInteger->uLength;
    AnscCopyMemory(pPrivate->PublicExponent.Data.ucData, pInteger->pValueBuffer,pInteger->uLength);

    pInteger    = (PANSC_ASN1_INTEGER)pRSA->GetChildByIndex(pRSA,3);
    pPrivate->PrivateExponent.Length = pInteger->uLength;
    AnscCopyMemory(pPrivate->PrivateExponent.Data.ucData, pInteger->pValueBuffer, pInteger->uLength);

    pInteger    = (PANSC_ASN1_INTEGER)pRSA->GetChildByIndex(pRSA,4);
    pPrivate->Prime1.Length = pInteger->uLength;
    AnscCopyMemory(pPrivate->Prime1.Data, pInteger->pValueBuffer, pInteger->uLength);

    pInteger    = (PANSC_ASN1_INTEGER)pRSA->GetChildByIndex(pRSA,5);
    pPrivate->Prime2.Length = pInteger->uLength;
    AnscCopyMemory(pPrivate->Prime2.Data, pInteger->pValueBuffer, pInteger->uLength);

    pInteger    = (PANSC_ASN1_INTEGER)pRSA->GetChildByIndex(pRSA,6);
    pPrivate->Exponent1.Length = pInteger->uLength;
    AnscCopyMemory(pPrivate->Exponent1.Data.ucData, pInteger->pValueBuffer, pInteger->uLength);

    pInteger    = (PANSC_ASN1_INTEGER)pRSA->GetChildByIndex(pRSA,7);
    pPrivate->Exponent2.Length = pInteger->uLength;
    AnscCopyMemory(pPrivate->Exponent2.Data.ucData, pInteger->pValueBuffer, pInteger->uLength);

    pInteger    = (PANSC_ASN1_INTEGER)pRSA->GetChildByIndex(pRSA,8);
    pPrivate->CoEfficient.Length = pInteger->uLength;
    AnscCopyMemory(pPrivate->CoEfficient.Data.ucData, pInteger->pValueBuffer, pInteger->uLength);


    /* init the decryption */
    Op  = ANSC_CRYPTO_PUB_OPERATION_DECRYPT;
    Alg = ANSC_CRYPTO_PUB_ALGORITHM_RSA_ENCRYPTION;
    pParams->Key.RSA.RsaPrivateKey    = *pPrivate;

    pParams->InputSize  = lengthOfData;
    pParams->pInput     = (PVOID)pDataWillBeDecrypted;

    pParams->OutputSize = *pLength;
    pParams->pOutput    = (PVOID)pDataDecrypted;

    status = 
        pCrypto->Request
            (
                Alg, 
                Op, 
                (ANSC_HANDLE)pParams, 
                NULL, 
                NULL
            );

    if(pLength)
    {
        *pLength = pParams->OutputSize;
    }

    pCrypto->Remove(pCrypto); 
    AnscFreeMemory(pParams);

    return status;
}

